/*
 * text_to_graph
 *   DESCRIPTION: Stores text color for pixels in correct position in a 
 *                buffer that has inverted plane order (like build buffer). 
 *                Makes use of a helper function detect_ones.
 *                the logical view window.
 *   INPUTS: str -- This is an array of characters which is the string input.
 *                  Spaces count as chracters in this string.
 *           buf -- Creates a multidimensional array of size 4*1440 representing
 *                  the VGA planes in the buffer. Plane order (from beginning) is 3,2,1,0.
 *   OUTPUTS: Void function has no output but changes the buffer input.
 *   RETURN VALUE: None
 *   SIDE EFFECTS: Writes color data to the passed-in buffer
 */
void text_to_graph(char* str, unsigned char buf[4][1440], int bg_color)
{   
    int space_offset = (STATUS_WIDTH - CHAR_WIDTH*strlen(str))/CHAR_WIDTH ;
    int x,y,i,s,idx ; // loop indices initialization
    int result[CHAR_WIDTH] ; // array initialization
   

    /* Init all to light blue*/
    for (x = 0 ; x < PLANE_NUMS ; x++)
    {
        for (y = 0 ; y < PLANE_SIZE ; y++)
        {
             buf[x][y] = bg_color ;
        }
       
    }
    // 1 pixel below in status bar
        for (s = 0 ; s < strlen(str) ; s++)
        {    
            int letter = (int) str[s] ; // type-cast char into int
            for (idx = 0 ; idx < CHAR_HEIGHT ; idx++)
            {
                char bits = font_data[letter][idx] ; // access 8 bit value for each char and row
                detect_ones(bits,result) ; // function call to detect 1 bits
                for (i = 0 ; i < CHAR_WIDTH ; i++) // looo through the array
                {
                    if (result[i] != -1) // if not -1 then add text_color to buffer
                    {
                        buf[ 3 - (i & 3)][space_offset + Y_OFFSET + i/4 + (Y_OFFSET*idx)+ 2*s] = TEXT_COLOR ; // add text_color to appropriate pixel in buffer using offset
                    }
                }
            }
        }

}


/*
 * detect_ones
 *   DESCRIPTION: HELPER. Stores indices (starting from left) of the "1" bits in the
 *                8 bit num input and storing those indices in a size 8 array called
 *                res. Allows us to pinpoint the location of the 1's in the font data 
 *                8 bit numbers. 
 *   INPUTS: num -- 8 bit number whose 1's are to be detected.
 *           res -- The size 8 array that is to store the indices of the 1 bits otherwise
 *                  it stores -1 instead. 
 *   OUTPUTS: Void function has no output but modifies the array input.
 *   RETURN VALUE: None
 *   SIDE EFFECTS: Writes data to inputted array.
 */

void detect_ones(char num, int* res)
{   
    int k ;
    for (k = 0 ; k < CHAR_WIDTH ; k++)
    {
        res[k] = -1 ; //Init the array
    }
    int ct = 0 ;
    int counter = 0 ;
    while (counter < CHAR_WIDTH) //left shift at most 8 times
    {
        if (num < 0)
        {   
            res[counter] = counter ;  
            ct++; //if number is negative then increment counter
        }
        num=num<<1; //left shift original number
        counter++; //inc counter
    }
    
}


/*
 * fruit_text_to_graph
 *   DESCRIPTION: Intializes array to 0's and sets it to 1 at an index where the font data for the  
 *                corresponding character is 1. (1 means text 0 means no text) 
 *   INPUTS: fruit_number -- This allows one to create a formatted string based on the input fruit number.
 *           fruit_buf -- Creates a multidimensional array of size 16*104 representing the text to write 
 *                        above the player. 16 is the CHAR Height and 104 is maximum number of chars (13) times 8 
 *                        which is CHAR Width.
 *   OUTPUTS: Prints nothing to console. 
 *   RETURN VALUE: Length of formatted string. I use this for centering the text.
 *   SIDE EFFECTS: Writes text data to the passed-in buffer
 */
int fruit_text_to_graph(int fruit_number, unsigned char fruit_buf[16][104]) 
{
    int s_index,i,j,store[CHAR_WIDTH] ;
    char* fruit_string ;  //init vars

    for (i = 0 ; i < CHAR_HEIGHT ; i++)
    {
        for (j = 0 ; j < MX_CHAR_SIZE ; j++)
        {
            fruit_buf[i][j] = 0 ;  //init buffer
        }
    }

    if (fruit_number == 1) //format string based on fruit_num
    {
        fruit_string = "an apple!" ;
    }
    else if (fruit_number == 2)
    {
        fruit_string = "grapes!" ;
    }
    else if (fruit_number == 3)
    {
        fruit_string = "a peach!" ;
    }
    else if (fruit_number == 4)
    {
        fruit_string = "a strawberry!" ;
    }
    else if (fruit_number == 5)
    {
        fruit_string = "a banana!" ;
    }
    else if (fruit_number == 6)
    {
        fruit_string = "watermelon!" ;
    }
    else if (fruit_number == 7)
    {
        fruit_string = "YEAH! DEW!" ;
    }
    else
    {
        fruit_string = "" ; //else display nothing
    }

    for (s_index = 0 ; s_index < strlen(fruit_string) ; s_index++) //loop through string
        {    
            for (j = 0 ; j < CHAR_HEIGHT ; j++) //loop through each row
            {
                char bits = font_data[(int) fruit_string[s_index]][j] ; // access 8 bit value for each char and row
                detect_ones(bits,store) ; // function call to detect 1 bits
                for (i = 0 ; i < CHAR_WIDTH ; i++) // loop through the array
                {
                    if (store[i] != -1) // if not -1 then add "1" to buffer
                    {
                        fruit_buf[j][CHAR_WIDTH*s_index + i] = 1 ; // add "1" to appropriate pixel in buffer using row-major offset
                    }
                }
            }
        }
    return strlen(fruit_string) ; //return formatted string length
}


